#!/usr/bin/env python3
"""Generate ANSI pixel art: white peace-sign hand on purple background."""
import sys

# 44 cols × 28 rows — 1=hand(white), 0=bg(purple)
# Hand shifted left; purple space dominates upper-right quadrant
PIXELS = [
    "00000000000000000000000000000000000000000000",
    "00000000000000000000000000000000000000000000",
    "00000000000000000001100011000000000000000000",
    "00000000000000000011111111000000000000000000",
    "00000000000000000111000111100000000000000000",
    "00000000000000001100000001100000000000000000",
    "00000000000000011000000000110000000000000000",
    "00000000000000110000000000011000000000000000",
    "00000000000001100000000000001100000000000000",
    "00000000000011000000000000000110000000000000",
    "00000000000110000000000000000011000000000000",
    "00000000001100000001110000000001100000000000",
    "00000000011000000011111000000000110000000000",
    "00000000011000000111111100000000110000000000",
    "00000000110000011100011100000000011000000000",
    "00000000110000110000001110000000011000000000",
    "00000000110001100000000011000000011000000000",
    "00000000011011000000000000110000110000000000",
    "00000000011110000000000000011001100000000000",
    "00000000001100000000000000001111000000000000",
    "00000000000110000000110000001110000000000000",
    "00000000000011000011110000111100000000000000",
    "00000000000001100111000011110000000000000000",
    "00000000000000111110000011100000000000000000",
    "00000000000000011100000011100000000000000000",
    "00000000000000000111111111000000000000000000",
    "00000000000000000011111110000000000000000000",
    "00000000000000000001111100000000000000000000",
]

# ANSI true-color: vibrant purple bg + white hand
PURPLE = "\033[48;2;130;50;210m"
WHITE  = "\033[38;2;255;255;255m"
RESET  = "\033[0m"

# Use half-block trick: each pixel gets two character rows
# upper half = ▀, lower half = ▄
# This doubles vertical resolution

BLOCK = "█"

for row in PIXELS:
    # Build line with grouped ANSI codes for efficiency
    line = PURPLE  # set bg for whole line
    i = 0
    while i < len(row):
        if row[i] == "1":
            line += WHITE + BLOCK
            i += 1
        else:
            # Count consecutive bg spaces
            spaces = 0
            while i < len(row) and row[i] == "0":
                spaces += 1
                i += 1
            line += " " * spaces
    line += RESET
    print(line)

print()
